class BoostDiConan(ConanFile):
    name = "boost.di"
    homepage = "https://github.com/boost-experimental/di"
    description = "[Boost].DI: C++14 Dependency Injection Library."
    topics = ("conan", "boost", "boost.di", "dependency-injection")
    url = "https://github.com/conan-io/conan-center-index"
    license = "BSL-1.0"
    exports_sources = ["CMakeLists.txt"]
    generators = "cmake"
    settings = "os", "compiler", "build_type", "arch"
    options = {}
    default_options = {}

    @property
    def _source_subfolder(self):
        return "source_subfolder"

    @property
    def _build_subfolder(self):
        return "build_subfolder"

    def source(self):
        tools.get(**self.conan_data["sources"][self.version])
        extracted_dir = "di-" + self.version
        os.rename(extracted_dir, self._source_subfolder)

    def config_options(self):
        self.settings.clear()

    def package(self):
        with open("LICENSE", "w") as f:
            f.write(_bsl_1)
        self.copy("LICENSE", dst="licenses", src=self._source_subfolder)
        header_dir = os.path.join(self._source_subfolder, "include")
        dst_dir = os.path.join("include", "boost")
        self.copy("*.hpp", dst="include", src=header_dir)

    def package_id(self):
        self.info.header_only()
